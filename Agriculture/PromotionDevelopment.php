 <p><b>किसानों को संगठित करना :</b> जब कारोबार आयोजना प्रक्रिया
के माध्यम से किसानों के एक चयनित समूह की सहायता से
एसएचटी द्वारा स्थापित किए जाने का औचित्य प्रबल उदाहरण
से प्रमाणित हो जाता है, तो किसानों को एफआईजी में और अंततः 
एफपीओ के किसान - सदस्य के रूप में संगठित करना चाहिए।
किसानों को संगठित करने का कार्य संप्रेषण के अनेक तरह के
माध्यमों से किया जाना चाहिए जैसे कि प्रचार पुस्तिका, वृत्त चित्र, पोस्टर, ग्राम स्तर पर नियमित बैठकें, विकासशील किसान
- सदस्यों के दृष्टिकोण का समुचित विकास आदि। बढ़ावा देने
वाले किसान-सदस्य वे होते हैं जो स्वैच्छिक आधार पर एफपीओ
का गठन करने के लिए उत्सुक होते हैं, और एफपीओ के गठन
में संसाधन संस्थाओं के एसएचटी द्वारा प्रदान किए गए प्रशिक्षण
कार्यक्रमों एवं प्रदर्शन से प्राप्त होने वाले महत्व एवं संभावित लाभों
को समझते हैं ।</p>

<p><b>संगठित करना एवं औपचारिक रूप देना :</b> किसान लाभ समूह
किसी एकीकृत समूह में साथ मिलकर एफपीओ का गठन करते
हैं। विशिष्ट रूप से, एफपीओ का गठन करने के लिए 50 से
70 एफआईजी एक साथ शामिल हो सकते हैं। एफपीओ को कंपनी अधिनियम के अंतर्गत उत्पादक कंपनी के प्रावधानों के
अंतर्गत पंजीकृत किया जा सकता है। एफपीओं के पंजीकरण
पर एक अलग नियमावली इन दिशानिर्देशों के साथ संलग्न है।
अनुबंध-7 भी देखें। तथापि, यहस्पष्ट करने की जरूरत है कि
किसानों को संगठित करने का उद्देश्य केवल एक औपचारिक
संस्था को पंजीकृत करने के हक्ष्य को प्राप्त करना नहीं है।
एफपीओ द्वारा जो अंतिम स्वरूप ग्रहण किया जाएगा (अर्थात
सहकारी समिति, उत्पादक कंपनी, बहुराज्यीय सहकारी समिति
आदि) उसके बारे में निर्णय उपयुक्त समय पर एफआईजी के 
जाना चाहिए। इनके लिए मानक प्रचालन कार्यविधियां स्थापित की
जानी चाहिए।</p>

<p><b>कारोबार प्रचालन :</b> कारोबार प्रचालन एफपीओ की प्रापण, उत्पादन,
प्रसंस्करण, विपणन तथा वित्तीय सेवाओं से संबंधित गतिविधियों
की शुरूआत है। संसाधन संस्थाओं को चाहिए कि वे एफपीओ की
शासकीय एवं प्रचालन संस्थाओं दोनों को ध्यान से प्रशिक्षित करें
ताकि व्यवसाय प्रचालन के अबाध कार्यकरण सुनिश्चित हो सके।
विभिन्‍न कृषि एवं संबद्ध उत्पादों तथा पदार्थों से संबंधित संपूर्ण मूल्य
श्रृंखला का प्रबंधन करने की जरूरत होती है।</p>

<p><b>मूल्यांकन एवं लेखा परीक्षा :</b> संसाधन संस्थाओं को चाहिए कि
वे विभिन्‍न हितधारकों जैसे कि किसान- सदस्य, निर्देशकों के
शासी मंडल तथा सेवा प्रदाताओं के कार्य निष्पादन के नियमित
रूप से मूल्यांकन में सहायता प्रदान करें। उन्हें सुधार के क्षेत्रों को समझने के लिए संस्थानिक परिपक्वता सूचकांक का प्रयोग
करके इन्हें दर्शाने में एफ्पीओ की सहायता करनी चाहिए।
आंतरिक प्रक्रिया तथा लेखाओं की परीक्षा से पारदर्शिता एवं
जवाबदेही दोनों सुनिश्चितकरने में मदद मिलेगी (अनुबंध-9)।
ये एफपीओ के विकास के लिए प्रमुख संस्थानिक प्रणालियां हैं।</p>
