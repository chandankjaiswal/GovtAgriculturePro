				        <p><b>समूह की पहचान :</b> समूह के क्षेत्रों का चयन संसाधन संस्था
द्वारा राज्य सरकार के संबंधित विभागों के साथ परामर्श करके
किया जाएगा। तथापि, यहसुनिश्वित किया जाना चाहिए कि किसी
विशिष्ट जिले के 80 से 420 समीपस्थ गांवों की पहचान करते हुए
एक या दो ब्लॉक के अंदर 8,000 से 0,000 किसानों के समूह का
निर्माण किया जाए।</p>

<p><b>नैदानिक अध्ययन :</b> समूह के चयनित क्षेत्र में संसाधन संस्था द्वारा
नैदानिक अध्ययन किया जाएगा। किसानों की प्रारंभिक स्थिति
तथा क्षेत्र में कृषि के स्तर का मूल्यांकन करने के लिए नैदानिक
अध्ययन किया जाता है। इस अध्ययन से अपेक्षित संभावित हस्तक्षेपों
की पहचान करने में सहायता के साथ, विशिष्ट परियोजनाओं के
कार्यान्वयन के संदर्भ को समझने में भी सहायता मिलेगी। इस
अध्ययन में शामिल किए जाने वाले कारकों की विस्तृत सूची
अनुबंध-4 में दी गई है।</p>
<p><b>संभाव्यता विश्लेषण :</b> संसाधन संस्थाओं द्वारा एफपीओ के गठन
के लिए संभाव्यता विश्लेषण किया जाना चाहिए और फिर विभिन्‍न तकनीकी क्षेत्रों के लिए नियुक्त किए गए बाहरी विशेषज्ञों द्वारा
उनका मूल्यांकन किया जाना चाहिए। एक औपचारिक संभाव्यता
अध्ययन के तहत वित्तीय, तकनीकी, कानूनी, राजनीतिक, सामाजिक
- आर्थिक, पर्यावरणीय तथा संसाधन संभाव्यता जैसे पहलू शामिल
होने चाहिए। संभाव्यता विश्लेषण को एफपीओ के प्रचलित विशिष्ट
क्षेत्रीय एवं पर्यावरणीय संदर्भ में एफपीओ के संवर्धन का औचित्य
स्थापित होगा।</p>

<p>आधार रेखा मूल्यांकन : संसाधन संस्था द्वारा संचालित किए
जाने वाले आधार रेखा मूल्यांकन से खेती तथा छोटे, सीमांत एवं
काश्तकार किसानों की वर्तमान प्रचलित स्थिति से संबंधित आंकडे
सृजित करने में सहायता प्राप्त होगी। आधार रेखा मूल्यांकन के
तहत संभावित हस्तक्षेपों की पहचान करने, विकास एवं व्यवसाय की
योजनाएं तैयार करने तथा भावी परिणाम संकेतकों के आधार पर
बुनियादी आंकड़े स्थापित करने के लिए अनेक कारकों को शामिल
किया जाएगा, जिन्हें परिवर्तन में योगदान को समझने के लिए मापा
जा सकता है। अनेक तरह के हितधारकों के साथ खुली केन्द्रित
समूह चर्चा तथा सुगठित परिवार स्तरीय साक्षात्कार के माध्यम से श्रेणीबद्ध अनियमित नमूनाकरण का प्रयोग करके मूल्यांकन संचालित
किया जाएगा। आधार रेखा सर्वेक्षण में जिन कारकों का अध्ययन
किया जाना है उनकी विस्तृत जांच सूची के लिए अनुबंध-5 देखें।</p>

<p><b>कारोबार की आयोजना : </b>किसानों के चयनित प्रतिनिधियों की सहायता से संसाधन संस्थाओं द्वारा कारोबार की योजना तैयार
की जाएगी। कारोबार आयोजना एक ऐसी प्रक्रिया है जिसके माध्यम
से उभरते एफपीओ की रणनीति एवं प्रचालनात्मक अभिविन्यास
को आकार दिया जाता है। यद्यपि उस स्तर को समझने के लिए
आधार रेखा मूल्यांकन के आंकड़े महत्वपूर्ण निवेश होंगे जिस स्तर
पर किसान-सदस्यों के लिए उत्पादों और सेवाओं का विकास किया
जाना चाहिए, फिर भी एफपीओ के भविष्य की सामूहिक रूप से
कल्पना करना अधिक महत्वपूर्ण होगा। अनेक तरह के उपकरणों तथा सुव्यवस्थित सामूहिक विचार-विमर्शों का प्रयोग करके विभिन्‍न
पहलुओं पर समुचित अनुमान के साथ कारोबार योजना को विकसित
करने की जरूरत होती है। विस्तृत कारोबार योजनाएं विकसित
करने की कुंजी यह हैं कि एफपीओ के कम से कम 10 प्रतिशत
किसान - सदस्य अपना स्पष्ट नजरिया प्रदान करें। अंतर्वस्तु की
विस्तृत सूची अनुबंध-6 में दी गई है।</p>
