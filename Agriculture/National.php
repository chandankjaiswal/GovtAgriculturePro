        <div class="col-md-6">

                    <p><b>3. स्कोप और कवरेज</b><br>
<b>3.1</b> इस नीति के प्रावधान समान रूप से लागू होंगे
एफपीओ के तहत पहले से ही पंजीकृत या तो
कंपनी अधिनियम और विभिन्न केंद्रीय और के तहत
राज्य सहकारी सोसायटी कानून और उन एफपीओ
जो इस मुद्दे के बाद पंजीकृत किया जाएगा
इस नीति का।</p>

<p><b>3.2</b> एफपीओ को आकर्षित करने के लिए मुख्य योग्यता मानदंड
विभिन्न योजनाओं और कार्यक्रमों के तहत लाभ
केंद्र और राज्य सरकारों की यह है कि
एक पंजीकृत और प्रशासित निकाय होना चाहिए
किसानों और संगठन पर ध्यान केंद्रित किया जाना चाहिए
कृषि और संबद्ध क्षेत्रों में गतिविधियों पर।</p>

<p><b>4. एफपीओ के प्रचार के लिए समर्थन</b><br>
<b>4.1</b> एफपीओ का गठन और विकास होगा
सक्रिय रूप से प्रोत्साहित किया और समर्थन किया
केंद्र और राज्य सरकारें और उनकी एजेंसियां,
विभिन्न केन्द्र से वित्तीय संसाधनों का उपयोग करना
और राज्य पोषित योजनाओं में
कृषि क्षेत्र की एजेंसियां। यह लक्ष्य होगा
द्वारा भागीदारों का गठबंधन बनाकर हासिल किया
संबंधित प्रवर्तक निकाय, जिसमें सिविल सोसाइटी शामिल है
संस्थान, अनुसंधान संगठन, सलाहकार,
निजी क्षेत्र के खिलाड़ी और कोई अन्य संस्था जो
मजबूत और के विकास में योगदान कर सकते हैं</p>
