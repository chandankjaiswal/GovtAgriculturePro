              <p>1. दर्शन: एक समृद्ध और टिकाऊ बनाने के लिए
कृषि क्षेत्र को बढ़ावा और समर्थन देकर
सदस्य के स्वामित्व वाले निर्माता संगठनों, कि
किसानों को उत्पादकता बढ़ाने में सक्षम बनाना
कुशल, लागत प्रभावी और स्थायी संसाधन उपयोग
और अपनी उपज के माध्यम से उच्च रिटर्न का एहसास
सरकार द्वारा समर्थित सामूहिक कार्रवाई,
और शिक्षा, अनुसंधान के साथ उपयोगी सहयोग
एजेंसियों, नागरिक समाज और निजी क्षेत्र।</p>

 <p><b>2. मिशन</b><br>
<b>2.1</b> आर्थिक रूप से व्यवहार्य, लोकतांत्रिक, और को बढ़ावा देने के लिए
किसान उत्पादक संगठनों को स्वशासन
(एफपीओ)<br>
<b>2.2</b> ऐसे एफपीओ के प्रचार के लिए सहायता प्रदान करना
योग्य और अनुभवी संसाधन संस्थानों द्वारा</p>
<p><b>2.3</b> आवश्यक सहायता और संसाधन प्रदान करने के लिए -
नीति कार्रवाई, जानकारी, तकनीकी ज्ञान, वित्तीय
संसाधनों, और बुनियादी ढांचे - इन को मजबूत करने के लिए
एफपीओ।</p>
<p><b>2.4</b> किसानों की पहुंच को सक्षम करने में बाधाएं दूर करना
खरीदारों के रूप में, उनकी एफपीओ के माध्यम से बाजार
और विक्रेता।</p>

<p>
<b>2.5</b> अपने सामूहिक उत्पादन और विपणन शक्ति का लाभ उठाने के लिए एफपीओ में निवेश के लिए एक सक्षम नीति वातावरण तैयार करना।</p>

