<p>तथापि इन आरंभिक चुनौतियों से निजात पाने के लिए एएसए द्वारा
संवर्धित उत्पादक कंपनियों ने बचत एवं परिचय पत्र का सृजन करने से पूर्व आरंभिक दो वर्षों में कारोबार के एक भिन्न प्रतिरूप का
अनुसरण किया है जो इस प्रकार हैं
</p>

<p>◊  उत्पादक कंपनियों ने ऐसे कारोबार में कदम रखा जिसके लिए कम कार्यकारी पूंजी की या शून्य कार्यकारी पूंजी की जरूरत थी। इस तरह के चार उदाहरण यहां दिए गए हैं।</p>

     
	 <p>◊ अपने सदस्यों को तथा गैर सदस्यों को भी बीज एवं खाद
जैसे कृषि निवेश की आपूर्ति करने का कारोबार। इस मामले में
उत्पादक कंपनियों ने सार्वजनिक एवं निजी कंपनियों से खाद
एवं बीज के लिए व्यापारिक समझौता प्राप्त किया था तथा
नकद आधार पर सदस्यों को तथा गैर सदस्यों को भी सामग्री
की आपूर्ति करने में उन कपनियों की ओर से कमीशन एजेंट
के रूप में कार्य किया। बड़े पैमाने पर कारोबार होने के कारण
उत्पादक कंपनियां अच्छा मुनाफा कमाने में सफल हुई तथा वे
उन कंपनियों के साथ एक कारोबारी संबंध स्थापित करने में
भी सफल हुईं जिसकी वजह से उन्होंने आने वाले वर्षों में उन
कंपनियों से ऋण सीमा प्राप्त करने में सफलता प्राप्त की।
</p>

<p>◊ कारोबार प्रतिरूप के रूप में कृषि उत्पाद के प्रापण के
मामले में भी ऐसा ही अनुभव रहा, जब उत्पादक कंपनियों
ने संभावित क्रेताओं की पहचान की तथा उनसे वापसी क्रय
बैंक गारंटी का इंतजाम किया। चूंकि उत्पाद को खेत पर
ही बेचा गया इसलिए उत्पादक कंपनी के स्तर पर कोई
परिवहन एवं भंडारण लागत शामिल नहीं था। उत्पादक
कंपनियों ने क्रेताओं एवं विक्रेताओं (सदस्यों एवं गैर सदस्यों
दोनों) के बीच पारदर्शी लेनदेन का सुनिश्चित किया और
ऐसा करके उन्होंने क्रेताओं से कुछ मुनाफा कमाया।
</p>

<p>◊ अनेक उत्पादक कंपनियों ने भारत सरकार की ऐसी स्कीम
का लाभ उठाया, जो माल गोदाम की रसीद (डब्ल्यूएचआर)
को गिरवी रखने पर ऋण देती है। इस स्कीम के अनुसार,
डब्ल्यूएचआर को गिरवी रखने पर बैंकर उत्पाद के 80
प्रतिशत मूल्य तक ऋण देते हैं। इसके लिए किसी
अनुप्रांसगिक की जरूरत नहीं होती है। मुख्य रूप से
इसका प्रयोग ऐसे समय में बीज पैदा करने के कार्य के
लिए किया गया जब उत्पादक कंपनियों को छः माह से
अधिक अवधि के लिए कच्चे बीजों का भंडारण करना
पड़ता था। बीजों का उत्पादन सदस्यों द्वारा किया गया
तथा भुगतान दो किस्तों में किया गया है (क) प्रापण के
समय भुगतान का 80 प्रतिशत, और (ख) बिक्री की आय
प्राप्त करने के बाद 20 प्रतिशत। चूंकि इस गतिविधि से
लगभग 25 से 30 प्रतिशत मुनाफा प्राप्त हुआ इसलिए
उत्पादक कंपनियां बीज उत्पादकों को अग्रिम में अच्छे
प्रीमियम की घोषणा कर पाई, जिसके लिए उत्पादक शेष
20 प्रतिशत का भुगतान देर से प्राप्त करने के लिए सहमत
हो गए।
</p>

<p>◊ उत्पादक कपनियों ने वाटर पंप सेट, यंत्रीकृत हल, ट्रैक्टर
आदि जैसे विभिन्न कृषि औजारों के लिए विभिन्न कपनियों
से डीलरशिप प्राप्त किया, जिसे उन्होंने तर्कसंगत कीमत
पर सदस्यों को बेचा तथा लाभ / कमीशन के रूप में अच्छी खासी कमाई की। इन सामानों का भंडारण करने की भी
कोई जरूरत नहीं थी। मांग पर इन औजारों की आपूर्ति
की गई।
</p>

<p>उपर्युक्त उदाहरणों से मुख्य रूप से निम्नलिखित सबक प्राप्त किए जा सकते हैं </p>

<p>◊  शुरुआती वर्ष के दौरान कारोबार की ऐसी गतिविधियों का
चयन करें जिसके लिए बहुत कम पूंजी या शून्य पूंजी की
जरूरत हो तथा जिनमें कोई जोखिम भी न हो। सामान्य तौर
पर ट्रेडिंग की गतिविधि में इस तरह के अवसर उपलब्ध होते
हैं। यहां ध्यान देने वाली बात यह है कि कृषि व्यवसाय में
अधिकांश बिचौलिये अपनी खुद की पूंजी बहुत कम मात्रा में
लगाते हैं।
</p>

<p>◊ शेयर धारकों तथा अन्य लोगों के साथ विश्वसनीयता का
निर्माण करने के लिए यह आवश्यक है कि उत्पादक कंपनियां
जल्दी से जल्दी अपनी सफलता का प्रदर्शन करें। इसलिए
उत्पादक कंपनियों के लिए यह अपरिहार्य है कि वे कुछ छोटे
कार्य से शुरुआत करें तथा ऐसी गतिविधियां शुरू करें जिनमें
जोखिम कम हो तथा वे प्रबंधन के दृष्टि से बहुत जटिल न
हों।
 </p>

<p>◊  उत्पादक कपनियों के लिए निष्पक्ष कारोबार की प्रथाओं को
प्रदर्शित करना बहुत महत्वतपूर्ण है, जिसकी प्रशंसा सदस्यों
तथा व्यापार एवं उद्योग जगत दोनों द्वारा की जाती है, जिनके
साथ उत्पादक कंपनियां कारोबार करती हैं।
</p>


