<p>यह उत्पादक कंपनी की बुनियादी गतिविधियों को शुरू करने के लिए
अपेक्षित आरंभिक राशि है, जैसे कि आरंभिक निवेश जिसकी जरूरत
मुख्य रूप से उत्पादकों से कच्चे उत्पाद की खरीद एवं बिक्री, भंडारण,
परिवहन, प्रसंस्करण, बीमा आदि के लिए होती है। पूंजी की मात्रा
उत्पादक कंपनी के कारोबार के स्वरूप एवं मात्रा पर निर्भर होती है।
यह उत्पादक कंपनी की कार्यकारी पूंजी का हिस्सा होती है।
</p>

 <p><b>उपर्युक्त मदों के लिए पूंजी की आवश्यकता की गणना करते समय

निम्नलिखित बातों को ध्यान में रखने की जरूरत होती हैं </b></p>

<p>◊ उत्पादकों की संख्या/जमीन/ उत्पादों की संख्या तथा
इसकी माहवार उपलब्धता।
</p>

<p>◊ अभधिग्रहीत किए जाने वाले कच्चे उत्पाद की कुल अपेक्षित
मात्रा (माहवार)।
</p>

<p>◊ गतिविधि की अवधि [प्रापण से लेकर बिक्री तक के दिनों की
संख्या)।
</p>

<p>◊ क्रय मूल्य, विक्रय मूल्य।</p>

<p>◊ उत्पादकों के साथ ऋण सीमा तथा क्रेताओं की ऋण सीमा।</p>

<p>◊ मासिक भंडारण, हाथ में नकद तथा बाहर नकद का अनुमान,
अवधि एवं लागत।
</p>

<p>◊ परिवहन लागत (उत्पादकों से कंपनी तक तथा कंपनी के
स्टोर से क्रेताओं तक)।
</p>

<p>◊ श्रेणीकरण / प्रसंस्करण।</p>

<p>◊ बीमा</p>

<p>◊ पैकेजिंग की लागत, यदि कोई हो।</p>

<p>◊ विपणन की लागत, यदि कोई हो।</p>

<p>◊ कोई अन्य, लागत जो क्षेत्र विशिष्ट होती है (जैसे कि
सांविधिक अपेक्षा, उदाहरण के लिए कुछ राज्यों में एपीएमसी
के बाहर कृषि उत्पाद के लिए क्रेताओं को किसानों से सीधे
खरीदने के लिए एपीएमसी को कतिपय लेवी का भुगतान
करना होता है)।
</p>

 <p>उपर्युक्त मानदंडों के समालोचनात्मक मूल्यांकन से पूंजी संबंधी
आवश्यकता का विश्वसनीय अनुमान उपलब्ध होना चाहिए।</p>

<p>एएसए के अनुभव से संकेत मिलता है कि उत्पादक कंपनी के लिए
वास्तविक चुनौती कार्यकारी पूंजी की आरंभिक आवश्यकताओं को
जुटाने से संबंधित है।
</p>

<p><b> ऐसा निम्नलिखित कारणों से है </b></p>

<p>◊ किसी भी वित्तीय संस्था के लिए उत्पादक कंपनी वाणिज्यिक संस्था
होती और इसलिए वे उत्पादक कंपनियों से ऋण के प्रयोग में अन्तर
धन राशि के अंशदान की अफ्ता करती है। वित्तीय तंगी के कारण
उत्पादक कंपनी के लिए इसकी व्यवस्था करना कठिन होता है।
</p> 

<p>◊ ऋण के लिए उत्पादक कंपनियों से अनुप्रांसगिक उपलब्ध कराने
की भी अपेक्षा होती है तथा यह उत्पादक कंपनी जैसी नई कारोबार
संस्था के लिए अ्चन के रूप में होता है।
</p>
 <p>◊ शुरू में उत्पादक कंपनियों के पास सफल कारोबार करने के लिए
कोई प्रत्यय पत्र नहीं होता है तथा इसकी वजह से भी क्तिीय
संस्थाएं क्तिपोषण करने में असहजता का अनुभव करती है।
</p>


