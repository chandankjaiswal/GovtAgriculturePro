 <p>सदस्यता की प्रक्रिया के बारे
में उत्पादकों को समझाने की जरूरत होती है। सामान्य तौर
पर शेयर का मूल्य 0 रुपए प्रति शेयर रखा जाता है। प्रत्येक
सदस्य से शेयर पूंजी का अंशदान उत्पादकों की आर्थिक स्थिति
पर निर्भर होता है। एएसए द्वारा विकसित उत्पादक कंपनियों
में प्रति सदस्य शेयरों की संख्या 00 से 200 के बीच होती है।
कुछ उत्पादक कंपनियों में सदस्यों को समान संख्या में शेयरों का वितरण किया गया है, जबकि कुछ मामलों में इसमें अंतर
है। अधिनियम में प्रति सदस्य शेयरों की संख्या पर कोई प्रतिबंध
नहीं है। तथापि यह सुझाव दिया जाता है कि उत्पादक कंपनी
की शक्ति संरचना में संतुलन स्थापित करने के लिए सदस्यों
के बीच शेयरों का वितरण समान रूप से किया जाना चाहिए।

</p>

<p>◊ समुदाय के पात्र सदस्यों सदस्यता आवेदन पत्र (जो अधिनियम
में विनिर्दिष्ट हैं) के माध्यम से निदेशक मंडल के पास आवेदन
करने की अपेक्षा होती है। सदस्यता के आवेदन को स्वीकार या अस्वीकार करने के लिए सामान्य निकाय (जीबी) अंतिम
प्राधिकरण है।
</p>
<p>◊  जब संकल्पना को अच्छी तरह से स्वीकार कर लिया जाता
है, तो सामान्य समझ के आधार पर सदस्यों के साथ परामर्श
करके एक कारोबार योजना तैयार की जाती है। कारोबार
प्रस्ताव, इसकी व्यवहार्यता, बाजार संबंधी अवसर, व्यवसाय
का आकार तथा नए उद्यमों के संभावित लाभ आदि के बारे में
संभावित सदस्यों को समुचित रूप से बताया जाना चाहिए।
अध्याय 4 में कारोबार योजना विकसित करने की प्रक्रिया पर
विस्तार से चर्चा की गई है।
</p>

<p>◊  इसके साथ ही सदस्यों के साथ परामर्श करके तथा उनकी
सहायता से शुरुआत कर्ता उत्पादक कंपनी के प्रत्येक
पदाधिकारी की भूमिकाओं एवं जिम्मेदारियों को निर्धारित करते
हुए 'संगम ज्ञापन एवं संगम अनुच्छेद' का प्रारूप तैयार करता
है। शेयर धारकों को कंपनी के लिए अधिकृत पूंजी2 तथा
प्रत्येक शेयर की लागत को भी अंतिम रूप देना होता है।
शेयर की लागत तथा प्रति सदस्य शेयरों की संख्या को अंतिम
रूप देते समय आर्थिक दृष्टि से वंचित शेयर धारकों की
भुगतान करने की क्षमता पर भी विचार किया जाना चाहिए।
</p>

<p>◊  जब ये दस्तावेज तैयार हो जाएं, तो शेयर धारकों की पहली
औपचारिक बैठक का आयोजन करना चाहिए। इस बैठक की
मूल कार्यवाहीसंगम ज्ञापन एवं संगम अनुच्छेद तथा कंपनी के
निदेशक मंडल के चयन/निर्वाचन पर अनुमोदन प्राप्त करना
होता है। तथापि, यहां शुरुआत कर्ता के लिए यह उचित होता
है कि वे इस चरण पर चुनाव से बचें क्योंकि इससे सदस्यों के
बीच दरार उत्पन्न हो सकती है। तथापि यदि चुनाव के लिए
स्थितियां अनुकूल हों, तो शुरुआत कर्ता चुनाव करा सकता
है क्योंकि चुनाव की प्रक्रिया अपनाने से लोकतांत्रिक प्रक्रिया
एवं पारदर्शिता में वृद्धि होती है।
</p>


<p>◊  कंपनी के निदेशकों की चयनित सूची तथा संगम ज्ञापन
एवं संगम अनुच्छेद पर सदस्यों की सहमति प्राप्त कर लेने के बाद शुरुआत कर्ता अब पंजीकरण की प्रक्रिया शुरू कर
सकता है। शेयर धारकों के माध्यम से इकट्ठा किए गए
धन का प्रयोग पंजीकरण शुल्क तथा प्रोसेसिंग से संबंधित
अन्य शुल्क जैसे कि कंपनी सेक्रेटरी के लिए शुल्क, लेखन
सामग्री, यात्रा व्यय आदि के लिए किया जा सकता है।
लेखाबही में इन खर्चों को शेयर पूंजी से लिए गए ऋण के
रूप में दर्शाया जा सकता है। जब कंपनी अपने कारोबार
के माध्यम से संसाधन जुटा लेगी, तब इस ऋण को चुकता
किया जा सकता है।</p>


<p>◊  कंपनी का पंजीकरण हो जाने के बाद शेयर धारकों की पहली
आम बैठक पंजीकरण के 90 दिन के अंदर अनिवार्य रूप
से बुलायी जानी चाहिए। कारोबार योजना पर चर्चा करने
के अलावा, सामान्य निकाय को अगले कार्यकाल के लिए
निदेशक मंडल का चयन/निर्वाचन करना होता है। बैठक
के 60 दिन के अंदर अंतिम रूप दिए गए निदेशक मंडल
की सूची के साथ बैठक की कार्यवाही का विवरण कंपनी
रजिस्ट्रार (आरओसी) को भेजना चाहिए।
</p>



