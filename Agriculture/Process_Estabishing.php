<p> यह चरण कंपनी को कानूनी तौर पर पंजीकृत कराने की प्रक्रिया
से पूर्व घटित होता है। जैसा कि पहले बताया गया है, उत्पादकों
को संगठित करने की प्रक्रिया पूरी तरह से संदर्भ विशिष्ट होती है
तथा यह हर संदर्भ में भिन्न हो सकती है। जो कारक उत्पादकों
को संगठित करने की दिशा में योगदान कर सकते हैं वे अनेक हैं
तथा कभी-कभी बहुत जटिल होते हैं। तथापि ऐसा अनुभव किया
गया है कि क्षेत्र में प्राथमिक समूहों की मौजूदगी, बाहरी एजेंसी का
संपर्क तथा स्थानीय परिस्थिति एवं मुद्दों के बारे में उनकी समझ
उत्पादकों को कारगर ढंग से संगठित करने में महत्वपूर्ण भूमिका
निभाती है। तथापि यहां विस्तृत सामान्य चरणों का उल्लेख किया
गया है जिनका किसी शुरुआत कर्ता को उस समय अनुसरण
करना चाहिए जब वे उत्पादक कंपनी का गठन करने के लिए
उत्पादकों को संगठित करने का प्रयास आरंभ करते हैं। ये चरण
न तो अनुक्रम में हैं और न ही अकाट्य। परिस्थिति के आधार पर
ये चरणें एक ही समय पर हो सकते हैं। अपनाए जाने वाले चरण
निम्नलिखित हैं
</p>

 <p>◊  उत्पादक कंपनी का गठन करने से पूर्व शुरुआत कर्ता को
इस बारे में बिल्कुल स्पष्ट होना चाहिए कि व्यवसाय का
उद्देश्य तथा संभावना क्या है। प्रचालन के क्षेत्र, उत्पादकों
के प्रकार एवं संख्या, भूमि की आवश्यकता संबंधी मूल्यांकन,
अवसंरचना, कारोबार की मात्रा, कार्यकारी पूजी की
आवश्यकता, वित्तीय व्यवहार्यता, निगमन की प्रक्रिया आदि
के लिए अच्छी तरह से तैयारी करनी चाहिए। संक्षेप में,
शुरुआत कर्ता के पास पहले से ही एक खाका या योजना
होनी चाहिए। कहने की जरूरत नहीं है कि इन कार्यों को
पूरा करने में शुरुआत कर्ता को पेशेवर दृष्टिकोण अपनाना
चाहिए तथा इसके लिए उसे बाहरी सहायता की जरूरत हो सकती है।</p>

<p>◊  समूह के आधार पर प्रचालन के क्षेत्र के चयन का अभिप्राय
कम से कम 12 से 15 गांवों के समूह को चुनने से है।
आम तौर पर लगभग 800 से 000 उत्पादकों का आकार
कृषि आधारित उत्पादक कंपनी के लिए एक अच्छा आकार
है, तथापि संभालने वाले उत्पादों के आधार पर यह संख्या
परिवर्तित हो सकती है। सामान्य तौर पर क्षेत्र एवं किसानों
का चयन उत्पाद, किसानों की आवश्यकता तथा उत्पादन एवं
विपणन की दृष्टि से किसानों के समक्ष मौजूद आम समस्याओं
के आधार पर किया जाता है।
</p>

<p>◊ शुरुआत कर्ता संभावित उत्पादकों के साथ अनेक बैठकों का संचालन करके, उनके साथ संबंध विकसित करके तथा उत्पादक कंपनी की संकल्पना प्रस्तुत करके प्रक्रिया
की शुरुआत करता है। उत्पादकों को संभावित जोखिमों
तथा शेयरधारकों पर उनके संभावित प्रभाव के साथ-साथ
उत्पादक कंपनी के संभावित सामाजिक-आर्थिक लामों के
बारे में भी जानकारी प्रदान की जाती है।
</p>

<p> ◊  जब संभावित सदस्य इस संकल्पना को समझ जाते हैं, तब
उत्पादकों के अभिचिद्चित समूह की समझ को और सुदृढ़
करने के लिए सफल उत्पादक कंपनियों के ज्ञानार्जन दौरे
का आयोजन किया जा सकता है। संभावित सदस्यों को
संकल्पनाओं एवं पद्धतियों के बारे में जानकारी प्रदान करने के
लिए ज्ञानार्जन दौरे को शिक्षण कक्ष में प्रशिक्षण की तुलना में
बेहतर पाया गया है। तथापि ज्ञानार्जन दौरे की योजना ध्यान से
तैयार की जानी चाहिए तथा किसी अनुभवी व्याक्ति द्वारा इसका
संचालन किया जाना चाहिए जो सही परिफ्रेश्य में चीज़ों को
समझाने में समर्थ हो। आगे चलकर, जब उत्पादक कंपनी का
निगमन हो जाता है, निम्नलिखित क्षेत्रों में (परंतु इतने तक ही
सीमित नहीं है) निदेशक मंडल के सदस्यों के लिए निम्नलिखित
प्रयोजनों के लिए औपचारिक प्रशिक्षण की जरूरत होगी - (क)
उत्पादक कंपनी के नियमों एवं विनियमों को समझना, (ख)
कंपनी रजिस्ट्रार की सांविधिक अपेक्षाएं, (ग) उत्पादक कंपनी की कारोबार योजना, (ध) सरकारी योजनाएं, (ड) नेतृत्व, (च)
लेखा तैयार करने एवं रिकार्ड रखने की बुनियादी बातें तथा
इसी तरह के अन्य पहलू जिनकी आवश्यकता महसूस की
जा सकती है। सुझाव दिया जाता हैं कि कार्यक्रम कलेंडर
समेत निदेशक मंडल के लिए एक क्षमता निर्माण योजना हर
साल तैयार की जाए और प्रवर्तक एजेंसी द्वारा समय-समय
पर उसकी समीक्षा की जाए। 

</p>

