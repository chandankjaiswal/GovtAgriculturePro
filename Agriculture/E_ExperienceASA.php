प्रदेश एवं बिहार के कम संसाधन वाले क्षेत्रों में 3800 से अधिक
छोटे एवं सीमांत किसानों की ॥6 उत्पादक कंपनियों को बढ़ावा
दिया है जिनकी औसत सदस्यता प्रति उत्पादक कंपनी तकरीबन
2000 किसान हैं।
</p>

 <p>इनमें से प्रत्येक उत्पादक कंपनी में एक व्यवसायिक प्रबंधन टीम है
जो संबंधित उत्पादक कंपनी के निदेशक मंडल (बीओडी) के निर्देश
के तहत प्रचालनों का संचालन करती है। प्रबंधन टीम की जिम्मेदारी
में व्यवहारिक सहायता प्रदान करके निदेशक मंडल की क्षमता का
निर्माण करना भी शामिल है। ये उत्पादक कंपनियां मुख्य रूप से
कृषि व्यवसाय के क्षेत्र में हैँ तथा इनका केंद्र बिन्दु फसल बीज
उत्पादन, कृषि उत्पादकों के प्रसंस्करण एवं विपणन एवं निवेश पर
अधिक प्रतिफल प्रदान करने वाली गतिविधियों पर हैं तथा अपने
सदस्यों को उच्च कोटि के बीजों की उपलब्धता भी सुनिश्चित
करती हैं। इनमें से प्रत्येक उत्पादक कंपनी का वार्षिक औसत
