प्रबंधन लागत के लिए सहायता प्रदान करने तथा उत्पादक कंपनी
को 5 लाख रुपए की कार्यकारी पूंजी के रूप में एक बारगी
सहायता प्रदान करने का निर्णय है। इसके अलावा, मध्य प्रदेश
सरकार गुण - दोष के आधार पर कारोबार की जरूरतों को पूरा
करने के लिए उन्हें भूमि एवं मशीनरी उपलब्ध कराने के लिए भी
सहमत हो गई है। इन सभी नीतिगत निर्णयों को सरकारी राजपत्र
में अधिसूचित किया गया है।
