हो सकते हैं :
</p>

<p>◊ प्रबंधन टीम तथा निदेशक मंडल में अधिक उत्साह</p>

<p>◊ बैंकिंग संस्थाओं से सहयोग जिन्होंने कार्यकारी पूंजी के लिए
उत्पादक कंपनी को बाधा मुक्त ऋण प्रदान किया और
</p>

<p>◊ स्पष्ट रूप से चिह्वित व्यवसायिक अवसर जो निवेश पर अधिक
