<p>कारोबार की आयोजना में पहला कदम कारोबार के अवसर की पहचान
करना है। इस मामले में कारोबार के प्रस्ताव का चयन पहले ही कर
लिया गया है अर्थात छोटे उत्पादकों के लिए कृषि व्यवसाय। कारोबार
के विशिष्ट अवसर की पहचान ज्यादातर अनुक्रियाशील प्रक्रिया है।
निम्नलिखित अनुच्छेदों में कारोबार प्रस्ताव तैयार करने के विभिन्न साधनों
पर चर्चा करने के लिए एक चरण दर चरण दृष्टिकोण अपनाया गया है।
</p>

<p><b>रचनात्मक कारोबार के अवसर की पहचान के स्वरूप </b></p>

 <p>◊ समस्या का समाधान करने वाले उत्पादों, सेवाओं का
विकासः पहला कदम ऐसा विचार इजाद करना है
जिससेउन समस्याओं का समाधान हो सके, जिनका
अनुभव किसानों द्वारा किया जाता है। उदाहरण के लिए
बड़े बाजार में कृषि उत्पाद की सामूहिक बिक्री या बीज,
खाद, कीटनाशक आदि जैसे कृषि निवेश का सामूहिक
क्रय तथा उत्पादकों को इसकी बिक्री एक रचनात्मक
विचार है क्योंकि इससे बिचौलियों की भूमिका कम होती
है तथा किसानों को उत्तम उत्पादों एवं सेवाएं सुनिश्चित
होती हैं।
</p>

<p>◊ बड़े पैमाने पर महसूस की गई किसी आवश्यकता को
पूरा करने के लिए नई प्रौद्योगिकी या सामग्री का उपयोग

भारत के लाखों किसान खेती के लिए हाथ से बनाए
गए औजारों जैसे कि बैलों द्वारा खींचे जाने वाले लकड़ी
के हल, बैलगाड़ी, हाथ से भूज़ी निकालना आदि का
प्रयोग करते हैं, क्योंकि आर्थिक दृष्टि से वे इन्हें खरीदने
में असमर्थ होते हैं। छोटे किसानों को किराए के भुगतान
के आधार पर ट्रैक्टर, भूसी निकालने वाला यंत्र आदि जैसे यंत्रीकृत उपकरणों की सेवाएं शुरू करने के प्रस्ताव
से परिवर्तन आ सकता है। इनके प्रयोग से खेती का
कार्य किफायती ढंग से किया जाता सकता है।
</p>

<p>◊ कृषि विस्तार सेवाओं के लिए मांग उत्पन करना : राज्य
की कृषि विस्तार सेवाओं की खराब गुणकत्ता के कारण
कृषि निदानशाला शुरू करने का प्रस्ताव प्रस्तुत किया
जा सकता है जहां तर्कसंगत कीमत पर किसानों को
व्यवसायिक विस्तार सेवाएं उपलब्ध कराई जा सकती हैं।
इसी तरह, फसल बीमा जैसे उत्पादों को शुरू करने से
किसानों को काफी लाभ हो सकता है।
 </p>
 
 <p>◊  नजरिया : यह भविष्य को देखने तथा सपने को अथक
रूप से साकार करने की सामर्थय है। उदाहरण के लिए
एनडीडीबी के डॉ. वर्गीज कुरियन ने दूध का उत्पादन
करने वाले लाखों किसानों को बाजार से जोड़ने की
संभावना देखी।
</p>
 
 
 <p> <b>3.1 छोटे समूहों की सोच में परिवर्तन ऐसी तकनीक
है, जिसका सामान्य तौर पर नए कारोबारों के लिए
प्रस्तावों के सृजन में प्रयोग किया जाता है। यह
प्रक्रिया दो चरणों में संपन्न की जाती है : </b>
</p>
 
 
 <p>◊  पहले चरण में प्रस्तावों की गुणवत्ता पर टिप्पणी किए बगैर भारी संख्या
में प्रस्ताव सृजित करने पर जोर दिया जाता है। समूह समन्वयक को
यह सुनिश्चित करना चाहिए कि प्रस्तावों का मूल्यांकन नहीं किया
जाता है बल्कि उन्हें विस्तार से रिकार्ड किया जाता है।
</p>

 <p>◊ अगले चरण में प्रस्तावों का मूल्यांकन किया जाता है तथा एक चयनित सूची तैयार की जाती है। मूल्यांकन के मानदंड व्यक्ति पर भी हो सकते हैं।</p>
 
 <p> <b>3.2 अवसर एवं जोखिम का विश्लेषण तथा कारोबार
के अवसर की पहचान </b>
</p> 
	
<p>जब प्रस्तावों की चयनित सूची तैयार हो जाती है, तो कारोबार के
अवसरों एवं जोखिमों की पहचान के लिए बाह्य कारोबार के परिवेश
के संदर्भ में समालोचनात्मक रूप से उनका मूल्यांकन किया जाना
चाहिए। इसे अवसर एवं जोखिम मूल्यांकन भी कहा जाता है तथा
इस बात के मूल्यांकन के लिए इसका प्रयोग किया जाता है कि क्या
कारोबार का प्रस्ताव आगे बढ़ाने के लिए उपयुक्त है। छांटे गए प्रत्येक
प्रस्ताव के लिए निम्नलिखित की दृष्टि से अवसरों एवं जोखिमों को
नोट करें
</p>

<p>◊ बाजार का आकार।</p>

