<p>◊ इसकी स्थिरता अर्थात उत्पाद/सेवा के लिए मांग
दीर्घावधिक है या पूरी तरह से अस्थायी हैं?
 </p>

<p>◊  बाजार किस हद तक विद्यमान सेवा / समाधान से असंतुष्ट है।
</p>

<p>◊  स्तर</p>

<p>◊ प्रतिस्पार्धा का स्तर (अधिक, मध्यम या कम) और कीमत एवं गुणकता के प्रति बाजार की संवेदनशीलता। </p>

<p>◊ परिमाण </p>

<p>◊  लाभ की मात्रा तथा प्रवेश /निकास से जुड़ी बाधाएं</p>

<p> ◊ सरकार की नीतियों में परिवर्तन जैसे कि सब्सिडी, सस्ती दर
पर निधियों की उपलब्धता आदि।
 </p>
 
 <p><b>3.3 जोखिमों का चित्रण एवं प्रबंधन </b></p>
 
 <p>जोखिमों एवं संभावित सुरक्षा पायों की पहचान अवसर एवं जोखिम
मूल्यांकन का अभिन्न अंग है। इसका उत्देश्य जोखिमों का पूरी तरह
से सफाया करना नहीं है (यह एक असंभव संकल्पना हैं), अपितु
इसका उत्देश्य उनकी पहचान करना तथा इस दृष्टि से मूल्यांकन
करना है कि क्या प्रचालनात्मक लोच के माध्यम से उन्हें नियंत्रित
किया जा सकता है या न्यूनतम किया जा सकता है। यदि जोखिम
या संकट नियंत्रण से परे महसूस हों, तो उस कारोबार प्रस्ताव को
पूरी तरह से नकारा जा सकता है। तथापि ध्यान देने वाली बात
यह है कि कारोबार शुरू करने के बाद भी कारोबार के माहौल में
आंतरिक एवं बाहरी दोनों रूपों में जोखिम बने रहते हैं। इसलिए
इनके उपशमन के लिए जोखिम प्रोफाइल एवं रणनीति विकसित
करना जरूरी है। इस रूपरेखा एवं रणनीति के विकास में मुख्य रूप
से पांच चरण शामिल होते हैं जो इस प्रकार हैं
</p>

<p><b>1.</b> कमाई करने वाले घटकों को प्राथमिकता प्रदान करना
पहला कदम कंपनी के कमाई करने वाले घटकों की पहचान
करना और फिर उनको चित्रित करना है। ये ऐसे कारक हैं जिनका कमाई पर सबसे अधिक प्रभाव होता है, यदि इनमें कोई व्यवधान उत्पन्न होता है। उदाहरण के लिए उत्पादक
कंपनियां मानसून पर बहुत अधिक निर्भर होती हैं तथा जिस
साल मानसून ठीक नहीं होता है उस साल उसकी कमाई पर
इसका काफी प्रभाव हो सकता है।
</p>

<p><b>2.</b>महत्वपूर्ण आधारभूत सुविधाओं की पहचान करना : अगला
कदम आधारभूत सुविधाओं की पहचान करना है-जिसमें
प्रक्रियाएं, संबंध, लोग, विनियम, संयंत्र, तथा उपकरण
शामिल हैं-उत्पादक कंपनी की जो आय अर्जित करने का
सामर्थयबढ़ाते हैं।
</p>

<p><b>3.</b>कमजोरियों की पहचान करना : अगला कदम मुख्य कमजोरियों
की पहचान करना है।
</p>

<p><b>4.</b>कमजोर कड़ियां, घटक क्या हैं, जिन पर अन्य सभी निर्भर हैं?
यह सभी उत्पादकों के लिए एकल क्रेता, उत्पादक कंपनी का
कोई कर्मचारी (जैसे कि मुख्य कार्यपालक अधिकारी) जिस
पर उत्पादक कंपनी का संपूर्ण प्रचालन निर्मर होता है आदि
हो सकते हैं। कमजोर घटकों की पहचान निम्नलिखित हैँ
</p>


<p>◊ कोई ऐसी बात जिस पर अनेक अन्य निर्मर है, कोई अड़चन।</p>

<p>◊ ऐसी प्रक्रियाएं जिनका कोई विकल्प नहीं है।</p>

<p>◊ अधिक जोखिम वाले भौगोलिक क्षेत्रों (जैसे कि बाढ़ ग्रस्त
क्षेत्र), तथा उत्पादों (उदाहरण के लिए दूध एवं सब्जियों
जैसी क्षरणशील वस्तुएं) के साथ साहचर्य |
</p>

<p>◊ महत्वपूर्ण आधार्मूत सुविधाओं तक असुरक्षित पहुंच की
व्यवस्था |
</p>
