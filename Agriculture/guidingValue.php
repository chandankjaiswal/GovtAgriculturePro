एवं लाभों के बारे में आम जनता - विशेष रूप से युवाओं तथा
सहमति निर्माताओं - को सूचना प्रदान करते हैं।
</p>

<p><b>छठवां सिद्धांत: एफपीओ के बीच सहयोग </b></p>
