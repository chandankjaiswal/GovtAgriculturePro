की साझी संपत्ति होता हैं। किसान - सदस्य आम तौर पर सदस्यता की एक शर्त के रूप में जमा की गई अपनी पूंजी पर
सीमित क्षतिपूर्ति, यदि कोई हो, प्राप्त करते हैं। किसान - सदस्य
निम्नलिखित में से किसी या सभी प्रयोजनों के लिए अतिरिक्त भाग
का आवंटन करते हैं : अपने एफपीओ का विकास करना, संभवत
बचत का गठन करके, जिसका कुछ न कुछ भाग अविभाज्य होगा;
एफपीओ के साथ सदस्यों के लेन-देन के अनुपात में उनको लाभ
प्रदान करना; और सदस्यों द्वारा अनुमोदित अन्य गतिविधियों की
सहायता करना।</p>


