</p>

<p><b>दूसरा सिद्धांत : किसान - सदस्य दृवाय़ लोकतांत्रिक नियंत्रण </b></p>
<p>एफपीओ लोकतांत्रिक संगठन हैं तथा ये अपने किसान - सदस्यों
द्वारा नियंत्रित होते हैं जो इनकी नीतियों का निर्धारण करने एवं
