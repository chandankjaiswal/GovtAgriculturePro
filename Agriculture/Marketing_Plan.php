<p>कारोबार के अवसर का चयन हो जाने के बाद बाजार का
विश्लेषण किया जाता है। विश्लेषण के लिए डाटा गौण स्रोतों
जैसे कि एपीएमसी का प्रापण, नीतिगत दिशानिर्देश, अन्यों द्वारा
संचालित विशिष्ट अध्ययन आदि से प्राप्त किया जा सकता है। है ताकि कारोबार के भविष्य की समालोचनात्मक रूप से जांच
की जा सके।
</p>
<p>बाजार विश्लेषण के तहत निम्नलिखित के बारे में ब्यौरे शामिल
होने चाहिए
</p>

 <p>◊ समग्र बाजार,</p>

<p>◊ बाजार में परिवर्तन,</p>

<p>◊  बाजार के अनुभाग, उनकी आकर्षण क्षमता, लाभप्रदता,</p>

<p>◊ लक्षित बाजार एवं ग्राहक, </p>

<p>◊ ग्राहकों का विवरण, </p>

<p>◊ प्रतिद्वंद्वी - प्रत्यक्ष एवं परोक्ष।| </p>

<p><b>बाजार के अवसरों तथा जोखिमों/संकटों का मूल्यांकन : </b> <br>
बाजार के विश्लेषण के बाद नियत्रंण किए जाने वाले उत्पादों की अच्छाइयों एवं कमजोरियों तथा संगठन के रूप में उत्पादक कंपनी

की अच्छाइयों एवं कमजोरियों का विश्लेषण किया जाना चाहिए।

इसके तहत निम्नलिखित पर ध्यान दिया जाना चाहिए
</p>

<p>◊ प्रतिद्वंद्वी के उत्पादों/सेवाओं की तुलना में कंपनी के
उत्पादों / सेवाओं का अनूठापन,
</p>

<p>◊ भुगतान की शर्तें,</p>

<p>◊ गुणकता,</p>

<p>◊ उत्पादक कंपनी में जनशक्ति की कीमत का निर्धारण एवं
उनका अनुभव,
</p>

<p>◊ बाजार में उत्पादक कंपनी की लोकप्रियता।</p>

<p>अवसर एवं जोखिम विश्लेषण के साथ कमजोरियों एवं अच्छाइयों के
विश्लेषण को एसडब्ल्यू ओटी विश्लेषण कहा जाता है। अवसर एवं
जोखिम विश्लेषण से कारोबार के बाह्य परिवेश का विश्लेषण करने
में मदद मिलती है जबकि अच्छाइयों एवं कमजोरियों के विश्लेषण
के तहत कारोबार के आंतरिक माहौल पर अर्थात उत्पादक कंपनी
के उत्पादों, संगठन के रूप में उत्पादक कंपनी, इसकी सक्षमताओं,
जोखिम बर्दाश्त करने का सामर्थय तथा नीतियों पर ध्यान दिया
जाता है। इस कवायद के अंत में अच्छाई एवं जोखिम विश्लेषण
सारणी 5 जैसा दिखता है।
</p>

<p><b>4. 1 विपणन की रणनीति का चयन करना </b><br>

बाजार के जिस अनुभाग पर उत्पादक कपनी का प्रबंधन लक्ष्य
करना चाहता है उसका चयन करने तथा एस डब्ल्यू विश्लेषण पूरा
करने के बाद विपणन की उपयुक्त रणनीति का चयन किया जाना
चाहिए। यह चयन कई तरह के कारकों पर निर्भर होता है जिसमें
निम्नलिखित शामिल हैं छवि जिसे उत्पादक कंपनी अपने उत्पाद
एवं संगठन के बारे में प्रस्तुत करना चाहती है, उत्पादक कंपनी के
बिक्री संबंधी उद्देश्य जैसे कि क्या उत्पादक कंपनी जल्दी से बाजार
में घुसना चाहती है अथवा वह बाजार में धीमी गति से घुसने से
संतुष्ट है आदि। उत्पादक कंपनी रणनीति के एक या अधिक
संयोजनों का चयन कर सकती है परंतु उसे 4 पी (उत्पाद, कीमत,
स्थान एवं संवर्धन - जिसे विपणन मिश्रण कहा जाता है) के सही मिश्रण की रणनीतिक रूप से योजना बनाने की जरूरत होती है,
ताकि विपणन की उपयुक्त रणनीति तैयार की जा सके।
</p> 
 <p>अगले खंडों में कुछ साधनों एवं प्रविधियों पर चर्चा की गई है,
जिन्हें कारोबार रणनीति तैयार करते समय देखा जा सकता है। ये
रणनीतियों की पूर्ण सूची होने की पुष्टि नहीं करते हैं, परंतु निश्चित
रूप से ये महत्वपूर्ण रणनीतियों की सूची हैं।
</p>

