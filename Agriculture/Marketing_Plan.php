 <p> <b>4.2 स्थापित करने की रणनीति </b>
<br>
जब किसी बाजार का विखंडन कर लिया जाता है तथा किसी
विशिष्ट खंड का चयन कर लिया जाता है, तो उत्पादक कंपनी को
बाजार के उस खंड में उत्पाद को स्थापित करना होता है। इसका
अभिप्राय यह है कि उत्पादक कंपनी को ग्राहकों को बताना होता
है कि वह क्या पेश कर रही हैं तथा उसका उत्पाद प्रतिद्रंद्विदियों के उत्पादों से कितना भिन्न एवं बेहतर है। स्थापित करने का कार्य तीन चरणों में किया जाता हैः
</p>

<p>◊ प्रतिद्र॑दियों के उत्पादों से अपने उत्पाद के लाभों की पहचान करना,</p>

<p>◊ सही रणनीति (रणनीतियों) का चयन करना, और</p>

<p>◊ अंत में बजार के प्रति अपनाए गए दृष्टिकोण का संकेत प्रदान
करना।
</p>

<p> <b>4.3 स्थापित करने का आधार </b>
<br>
यह बात बिल्कुल स्पष्ट है कि इसके बाद उसी उत्पाद को ग्राहकों
की विशिष्ट आवश्यकताओं के आधार पर भिन्न ढंग से स्थापित किया
जा सकता है। स्थापित करने के आधार को समझने के लिए हमें इस
मामले में उत्पादक कंपनी के उत्पादों की दृष्टि से कृषि निदानशाला
सेवा स्थापित करने पर नजर डालना चाहिए, जिसे वह एक निश्चित
क्षेत्र के किसानों को उपलब्ध कराने का इरादा रखती है
</p>

<p>◊ विशिष्ट उत्पाद की विशेषताएं : समस्या का विश्लेषण एवं
निदान, सस्ती दर पर समाधान, खेत पर सेवाएं उपलब्ध
कराना, निरंतर अनुवर्ती कार्रवाई,
</p>

<p>◊ लाभ, समस्या/समाधान या आवश्यकताएं : पंजीकरण के
बाद 24 घंटे के अंदर प्रदत्त सेवाएं, विशेषज्ञों द्वारा सुझाव,
कृषि रसायनों की आपूर्ति जो असली हैं तथा उचित दर पर
उपलब्ध कराए जाते हैं,
</p>

<p>◊ प्रयोग के विशिष्ट अवसर : खेत पर सेवाएं उपलब्ध, कराना।
ग्राहक समस्या एवं समाधान पर चर्चा करने के लिए विशेषज्ञों
को अपने खेत पर बुला सकता है।
</p>

<p>◊ प्रयोक्ताओं की श्रेणी : ये सेवाएं छोटे किसानों के लिए आदर्श
हैं, जिनकी जोतें छोटी होती हैं तथा खेती पर अधिक निवेश
नहीं कर सकते हैं।
</p>

<p>◊ अन्य किसी सेवा के विरुद्ध. कृषि निदानशाला सेवाएं सरकारी
विस्तार सेवाओं की तुलना में अधिक विश्वसनीय हैं।
</p>
<p> <b>4.4 कीमत एवं संवर्धन पर आधारित रणनीतियां </b> <br>
बाजार में पैठ बनाने के लक्ष्यों को प्राप्त करने के लिए कीमत
तथा संवर्धन की गतिविधियों पर खर्च का स्तर बहुत महत्वपूर्ण साधन हैं। उदाहरण के लिए, यदि उद्देश्य बहुत जल्दी से बाजार
के एक बड़े भाग को हथियाना हो, तो कम कीमत एवं आक्रामक
विज्ञापन के संयोजन की रणनीति अपनाई जा सकती है जिससे
अधिक मात्रा में उत्पादों को बेचा जा सके। विपणन की रणनीतियों
को अक्सर गुणवत्ता एवं कीमत के स्तर के आधार पर निर्धारित
करना पड़ता है जिसे यह अपने प्रतिद्रंदियों की तुलना में बाजार
के चयनित खंड को प्रस्तुत कर सकती है।
</p>
<p>चुनी गई रणनीतियों के आधार पर विपणन मिश्रण (विपणन के चार
पी) को तैयार किया जा सकता है तथा विपणन की योजना लिखी
जा सकती है। इसके तहत निम्नलिखित शामिल होने चाहिए.
</p>

<p>◊ लक्षित बाजार,</p>

<p>◊ प्रतिस्पर्धा,</p>

<p>◊ परिवेश,</p>

<p>◊ उत्पाद / सेवा,</p>

<p>◊ कीमत,</p>

<p>◊ स्थान,</p>

<p>◊ संवर्धन,</p>

<p>◊ आने वाले वर्ष में लक्षित बिक्री तथा अगले दो वर्षों के लिए
अनुमान।
</p>


