<p>कृषि एवं सहकारिता विभाग (डीएसी), कृषि मंत्रालय, भारत
सरकार ने राज्य सरकारों के साथ मिलकर 2011-12 के दौरान
सदस्य आधारित किसान उत्पादक संगठनों (एफपीओ) को
बढ़ावा देने के लिए एक प्रायोगिक परियोजना शुरू की, जिसे
लघु किसान कृषि व्यापार संगठन (एसएफएसी) के माध्यम से
कार्यान्वित किया गया। इस प्रायोगिक कार्यक्रम के तहत देशभर
में लगभग 250 लाख किसानों को राष्ट्रीय कृषि विकास योजना
(आरकेवीवाई) की दो उप योजनाओं अर्थात शहरी समूहों के लिए
राष्ट्रीय सब्जी पहल और बरसात के पानी से प्रभावित 60,000
गांवों के लिए दलहन विकास कार्यक्रम के तहत 250 एफपीओ
(प्रत्येक एफपीओ में सदस्य के रूप में औसतन 1000 किसान
होंगे) में संगठित करने का कार्य शामिल था। इस परियोजना
का उद्देश्य अनेक राज्यों में विभिन्‍न स्तरों पर किसानों, विशेष
रूप से छोटे उत्पादकों का समूह बनाना है जिससे खेती में  नई प्रौद्योगिकी के प्रयोग को बढ़ावा मिल सके, उत्पादकता में
सुधार हो, उत्पादक सामग्री एवं सेवाओं तक पहुंच में सुधार हो,
तथा किसानों की आय में वृद्धि हो जिससे संपोषणीय कृषि पर
आधारित उनकी जीविका सुदृढ़ हो।</p>

 <p>एसएफएसी समूह में शामिल संसाधन संस्थाओं (आरआई) के
माध्यम से इन एफपीओ की सहायता कर रहा है। संसाधन संस्थाएं
एफपीओ को प्रशिक्षण एवं क्षमता निर्माण से संबंधित विभिन्‍न उत्पादक
सामग्री प्रदान करती हैं, तथा इन निकायों को उत्पादक सामग्री
आपूर्तिकर्ताओं, प्रौद्योगिकी प्रदाताओं एवं बाजार के खिलाड़ियों से
जोड़ती हैं। एफपीओ की क्षमता निर्माण में निवेश दो वर्ष की अवधि
में किया जाएगा। कृषि एवं सहकारिता विभाग तथा राज्यों की ओर
से एसएफएसी इस परियोजना की निगरानी भी कर रहा है तथा
इसकी प्रगति पर सूचना भेज रहा है।
</p>

<p>प्रायोगिक कार्यक्रम पहले ही उत्साहवर्धक परिणाम दिखा चुका है
तथा इस समय 3 लाख से अधिक किसान, ग्राम स्तरीय किसान
लाभ समूहों (एफआईजी) में संगठित हो चुके हैं, जिन्हें पंजीकृत
एफपीओ में संघबद्ध किया जा रहा है। सामूहिक कार्रवाई के माध्यम
से किसानों को सशक्त बनाने के अलावा, जमीनी स्तर की ये
संस्थाएं खेती की प्रौद्योगिकी, उत्पादित सामग्री एवं ऋण के प्रेषण
के लिए तथा अच्छी कीमत प्राप्त करने के लिए अपने उत्पादन को
सामूहिक रूप से बाजार तक पहुंचाने के लिए प्रधान बिंदु के रूप
में उभर रही हैं।
</p>
