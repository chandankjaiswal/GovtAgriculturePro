 <p>कृषि एवं सहकारिता विभाग इन दिशानिर्देशों को, किसान उत्पादक
संगठनों के संस्थानिक विकास की प्रक्रिया को मुख्य धारा में लाने के
लिए जारी कर रहा है जिससे कि राज्यों को 42वीं पंचवर्षीय योजना
अवधि के दौरान राष्ट्रीय कृषि विकास योजना (आरकेवीवाई) के तहत
नियमित गतिविधि के रूप में एफपीओ के संवर्धन में सीधी सहायता प्रदान करने के लिए प्रोत्साहित किया जा सके। इन दिशानिर्देशों का
उद्देश्य एफपीओ के संवर्धन के लिए एक मानक प्रणाली विज्ञान का
अनुसरण करने में राज्यों की सहायता करना तथा सांकेतिक लागत
एवं एक निगरानी रूपरेखा भी प्रदान करना है। किसानों को संगठित
करने के लिए राज्य संसाधन संस्थाओं (जैसे कि गैर सरकारी संगठन, निजी कंपनियां, अनुसंधान निकाय, सहकारी समितियां,
किसान समूह) को सीधे नियुक्त कर सकते हैं (ऐसे मामले में, उन्हें
सलाह दी जाती है कि वे खुली बोली के मानदंडों का अनुसरण करें,
जिसका सुझाव इन दिशानिर्देशों में दिया गया है)। विकल्प के तौर
पर, वे अपनी ओर से उपयुक्त संसाधन संस्थाओं का समूह बनाने
के लिए एसएफएसी को आमंत्रित कर सकते हैं। तीसरा विकल्प
यह होगा कि राष्ट्रीय कृषि विकास योजना प्रमुखसे एसएफएसी को
आवश्यक बजट प्रदान करके राज्य की ओर से एफपीओ का संवर्धन
करने के लिए यह कार्य सीधे एसएफएसी को सौंप दिया जाए। राज्य अपने मनपंसद विकल्प चुनने के लिए स्वतंत्र हैं।</p>

<p>
