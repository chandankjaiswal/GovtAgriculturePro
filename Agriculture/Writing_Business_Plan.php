<p>अब चूंकि हम अपनी विपणन एवं वित्तीय योजनाओं को पूरा कर
चुके हैं, इसलिए अब हम कारोबार योजना लिखने के लिए तैयार हैं | </p>

<p><b>6.1 कारोबार योजना क्‍या होती है? </b></p>

<p>कारोबार योजना एक सारगर्भित दस्तावेज होता है जो कारोबार के
मिशन, आंतरिक एवं बाह्य परिवेश तथा पिछले विश्लेषण में पहचान
की गई समस्याओं के संबंध में रणनीति के घटकों को निर्दिष्टि
करता है। कोई जरूरी नहीं है कि रणनीति में हर बार संशोधन
होने के साथ कारोबार योजना लिखी जाए। इसे तब लिखना चाहिए
जब आप कोई नया उद्यम विकसित करें या कोई बड़ी नई पहल
शुरू करें। कारोबार योजना से अनेक महत्वपूर्ण प्रयोजन पूरे होते है </p>


 <p>◊  यह निर्धारित बाजारों में उद्यम की व्यवहार्यता का निर्धारण
करने में सहायता करती है। </p>

<p>◊  यह योजना की गतिविधियों के आयोजन में उद्यमी का
मार्गदर्शन करती है। </p>

<p>◊  वित्त पोषण/ निधि प्राप्त करने में यह एक महत्वपूर्ण साधन के
रूप में काम करती है। अच्छी तरह से लिखी गई कारोबार योजना विस्तृत मापदंड भी
उपलब्ध कराती है जिनके आधार पर आगे चलकर लक्ष्यों की दिशा
में प्रगति का मूल्यांकन किया जा सकता है तथा नियंत्रित निर्णय
लिए जा सकते हैं।
</p>

<p><b>6.2 कारोबार योजना के घटक क्या हैं? </b></p>

<p><b>परिचय तथा कार्यकारी सारांश: </b>विशिष्ट कारोबार योजना की
शुरूआत संक्षिप्त परिचय से होती है जिसके बाद एक कार्यकारी
सारांश दिया जाता है। कार्यकारी सारांश तब तैयार किया जाता
है जब पूरी योजना लिख ली जाती है। इसका उद्देश्य महत्वपूर्ण
श्रोताओं जैसे कि संभावित निवेशकों को प्रभावी ढंग से योजना के
बारे में बताना है ताकि वे इसे आगे भी पढ़ें। </p>

<p><b>उद्योग का विश्लेषण: </b>सामान्य तौर पर उद्योग का विश्लेषण
कार्यकारी सारांश के बाद प्रस्तुत किया जाता है। इस खंड में
महत्वपूर्ण सूचना दी जाती है- संग्रहण जिस पर पहले चर्चा हो
चुकी है-- जो उद्यम या योजना को समुचित संदर्भ में प्रस्तुत
करता है। </p>

<p><b>विपणन की योजना: </b>विषणन योजना कोई नई रणनीति विकसित
करने की दिशा में पहला कदम है। इसे उत्पादक कंपनी के
लक्ष्यों के संदर्भ में विकसित किया जाता है तथा यह बाह्य
परिवेश के वास्तविक मूल्यांकन पर आधारित होनी चाहिए, जैसा
कि पहले चर्चा की गई है। विषणन योजना को पहले लिखा
जाता है क्‍योंकि विपणन संबंधी निर्णय विशिष्ट रूप से अन्य
क्षेत्रों में संसाधन संबंधी आवश्यकताओं का निर्धारण करते हैं।
स्पशष्ट रूप से बाजार में बड़ी हिस्सेदारी प्राप्त करने संबंधी
निर्णय के लिए विभिन्‍न प्रकार के संसाधनों की भरपूर प्रतिबद्ध
ता की जरूरत होती है। आप अपने उत्पाद या सेवा का संवर्धन
एवं वितरण किस तरह से करना चाहते हैं - इसका आपके
संगठन, उत्पादन, मानव संसाधन एवं वित्तीय योजनाओं पर
स्पष्ट प्रभाव होता है। </p>

<p><b>वित्तीय योजना: </b>योजनाओं की इस समूची प्रणाली का आधार वित्तीय
योजना होती है। सामान्य तौर पर तीन वित्तीय क्षेत्रों पर चर्चा की
जाती है </p>

 <p>◊  वित्त पोषण स्वरूप </p>
 <p>◊  नकद प्रवाह विवरण</p>
 <p>◊  तीन साल की आय का विवरण</p>

<p>सामान्य तौर पर कारोबार योजना में एक परिशिष्ट शामिल किया
जाता है। आमतौर पर इस परिशिष्ट में समर्थक सूचनाएं, दस्तावेज
एवं ब्यौरे होते हैं जो संपूर्ण योजना के स्पष्ट संप्रेषण के साथ
दखल देते हैं। इस प्रकार की सूचनाओं के उदाहरण में मूल्य
सूची, आर्थिक भविष्यवाणी, जनसांख्यिकीय आधार सामग्री डाटा तथा
बाजार विश्लेषण शामिल होते हैं। </p>

<p><b>6.3 कारोबार योजना लिखने के लिए सुझाव </b></p>

<p>कारोबार योजना का पाठ संक्षिप्त होने के साथ ही इसमें यथासंभव
अधिक से अधिक सूचनाएं होनी चाहिए। यह विरोधाभासी प्रतीत होता
है परंतु आप संकेत शब्द दृष्टिकोण का प्रयोग करके इस दुविधा को
दूर कर सकते हैं। किसी कार्ड पर निम्नलिखित संकेत शब्दों को
लिख लें तथा कारोबार योजना लिखते समय उसे अपने सामने रखें </p>

<p>कौन / क्या / कहां / कब / क्यों / कैसे / कितना </p>

<p>कारोबार योजना के प्रत्येक खंड के शुरूआत में एक अनुच्छेद में इन
सभी प्रश्नों (संकेत शब्दों द्वारा पूछे गए) के उत्तर लिखें। इसके
बाद आगे प्रत्येक मत के बारे में अधिक जानकार प्रदान करते हुए
उस विवरण का विस्तार करें। </p>

<p>करोबार योजना का कोई निर्धारित आकार नहीं है। औसत आकार
3) से 40 पृष्ठ का हो सकता है, जिसमें समर्थक दस्तावेज का
खंड भी शामिल है। कारोबार योजना को अनेक खंडों में विभाजित
करें। कारगर कारोबार योजना लिखने के लिए अनुशासन, समय एवं
निजता की जरूरत होती है। </p>

<p>कारोबार योजना लिखते समय समर्थक दस्तावेजों की अपनी सूची
को संकलित करके आप समय की बचत कर सकते हैं। उदाहरण
के लिए, अपने कारोबार की कानूनी संरचना के बारे में लिखते
समय आपको अपने साझेदारी करार की प्रतिलिपि शामिल करने की
जरूरत महसूस हो सकती है। समर्थक दस्तावेजों की अपनी सूची में
साझेदारी करार को लिखें। अपनी योजना के इस खंड का संकलन
करते समय आपके पास पहले से ही आवश्यक दस्तावेजों की सूची
होगी। जैसे-जैसे आप आगे बढ़ेंगे, किसी अन्य सूचना का अनुरोध
कर सकते हैं जो आपके पास नहीं है, जैसे कि क्रेडिट रिपोर्ट | </p>

<p>पिछली बातों को ध्यान में रखकर अब आप अपनी कारोबार योजना
तैयार करने की शुरूआत करने के लिए तैयार हैं। कारोबार की
योजना तैयार करने की प्रक्रिया का अच्छी तरह से जायजा लेने के
लिए इस दस्तावेज को पूरी तरह से पढ़ें। </p>

<p><b>6.4 कारोबार योजना की सुझायी गई रूपरेखा </b></p>

<p><b>।. आच्छादन पत्र </b></p>

<p>यह कारोबार योजना के शीर्षक पृष्ठ के रूप में काम करती है। इसमें
निम्नलिखित होने चाहिए </p>

 <p>◊ उत्पादक कंपनी का नाम </p>

 <p>◊ कंपनी का पता </p>

 <p>◊ कंपनी का फोन नंबर (एरिया कोड शामिल करें) </p>
