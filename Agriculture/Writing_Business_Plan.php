 <p>◊ लोगो (यदि कोई हो) </p>

 <p>◊ सी ई ओ,/निदेशक मंडल के नाम, पद, पता, फोन नंबर
(एरिया कोड शामिल करें) </p>

 <p>◊ कारोबार योजना जाशी करने का महीना एवं वर्ष </p>

 <p>◊ व्यक्ति/ संगठन का नाम जिसने इसे तैयार किया। </p>

     
 <p><b>॥. कारोबार का संक्षिप्तम विवरण </b></p>
<p>यह कारोबार का संक्षिप्त विवरण प्रदान करता है। प्रयोजन क्या हैं?
यह सफल क्‍यों होगा? </p>

<p><b>III. विषय सूची </b></p>
<p>प्रमुख विषयों एवं संदर्भों की पृष्ठि संख्या। </p>

<p><b>lV. विपणन योजना </b></p>

<p>विपणन योजना में लक्षित बाजार पर बल के साथ संपूर्ण बाजार के
बे में सूचनाएं होती हैँ। यह लक्षित ग्राहकों की पहचान करने में
मदद करती है तथा उनको उत्पादों या सेवाओं की आपूर्ति करने
तथा सही ढंग से उत्पादों या सेवाओं को स्थापित करने के उपायों
का सुझाव देती है। </p>

<p>◊ <b>लक्षित बाजार: </b>ग्राहकों की खासियतों की पहचान करें। बताएं
कि कैसे परिणामों को प्राप्त किया गया है। जनसाख्यीकी
संबंधी प्रश्नावली तथा सर्वेक्षण के साथ समर्थक सूचनाएं।
बाजार के आकार का अनुमान लगाएं। </p>

<p>◊ <b>अतिप्पा्धाः </b> प्रत्यक्ष एवं परोक्ष प्रतिस््था का मूल्यांकन करें।
दर्शाएं कि आप कैसे होड़ में आगे निकल सकते हैं। स्थान,
बाजार तथा कारोबार के इतिहास की दृष्टि से प्रतिस्पर्धा का
मूल्यांकन करें। </p>

<p>◊ <b>स्थानः </b> बताएं कि किस ढंग से ग्राहक को उत्पाद एवं सेवाएं
उपलब्ध करायी जाएंगी। सांख्यिकीय रिपोर्टों, दर पत्र आदि
के साथ समर्थकारी निर्णय। </p>

<p>◊ <b> संबवर्धनः </b>विज्ञापन को किस तरह से लक्षित बाजार के साथ जोड़ा
जाएगा? दर पत्र, संवर्धनात्मक सामग्री तथा विज्ञापन अभियान के
लिए समय सीमा शामिल करें। </p>

<p>◊ <b> मूल्य निर्धार्णः</b> बाजार अनुसंधान तथा सेवा या उत्पाद की
लागत के आधार पर मूल्य, का निर्धारण किया जाएगा। बताएं
कि किस तरह से मूल्य संरचना तैयार की गई है तथा इसके
समर्थन में अनुसंधान से सामग्री प्रस्तुत करें। </p>

<p>◊ <b> उत्पादः </b>उत्पाद की डिजाइन एवं पैकेजिंग के संबंध में संकेत
शब्दों के उत्तर दें। आरेख एवं स्वामित्व अधिकार से संबंधित
सूचनाएं शामिल करें। </p>

<p>◊ <b>बाजार में प्रवेश करने का समयः</b> तय करें कि बाजार में कब
उतरना है तथा यह निर्णय किस तरह लिया गया है। </p>

<p>◊ <b>लक्षित बिक्री: </b>अगले तीन साल के लिए लक्षित बिक्री के बारे
में बताएं। पहले साल की बिक्री को माहवार प्रस्तुत किया जा
सकता है। </p>

<p>◊ <b> उद्योग की रुझानें: </b>वर्तमान रूझानों के बारे में जानकारी प्रदान
करें तथा यह भी बताएं कि कैसे बाजार परिवर्तित हो सकता 
है तथा बदलते परिवेश से किस तरह तालमेल स्थापित करने
का प्रस्ताव है।</p>

<p><b>V. वित्तीय दस्तावेज </b></p>

<p>ये पिछले, वर्तमान एवं प्रस्तावित वित्त पोषण को दर्शाने के लिए
प्रयोग में लाए गए रिकार्ड हैं। नीचे ऐसे प्रमुख दस्तावेजों का उल्लेख
किया गया है जिनको कारोबार योजना में शामिल करने की जरूरत
होती है। यदि इनको यहां उल्लिखित क्रम में शामिल किया जाता
है, तो काम आसान हो जाता है। <p>

<p>◊ <b> नकद प्रवाह विवरण (बजट): </b>यह दस्तावेज दर्शाता है कि रुपए
की दृष्टि से कारोबार योजना का अभिप्राय क्या है। यह एक
निश्चित अवधि में नकद अंतः प्रवाह एवं बहिर्प्रवाह को दर्शाता
है तथा इसका प्रयोग आंतरिक योजना के लिए किया जाता
है। नकद प्रवाह विवरण दर्शाता है कि कब और कितना नकद
आपके व्यावसाय में आना चाहिए और इससे बाहर जाना
चाहिए। </p>

<p>◊ <b> तीन साल की आय को दर्शाना: </b>अगले तीन वर्षों के लिए
आपकी कंपनी के बारे में आपके अनुमानों को दर्शाने वाला
एक प्रपत्र आय विवरण। पहले वर्ष के आंकड़ों के लिए प्रपत्र
नकद प्रवाह विवरण का प्रयोग करें तथा आर्थिक एवं उद्योग
की रुझानों के अनुसार अगले साल के आंकड़े प्रस्तुत करें। </p>

<p>◊ <b> समान रूप से विभाजित विश्लेषण: </b> समान रूप से विभाजित
बिंदु वह है जब किसी कंपनी के व्यय बिक्री या सेवा की मात्रा
से पूरी तरह से मेल खाते हैं। इसे कुल व्यय या कुल यूनिट
उत्पादन (जिसकी लागत उनकी बिक्री द्वारा प्राप्त आय के
बिल्कुल बराबर होती है) द्वारा सटीक रूप से समायोजित कुल
रुपए या राजस्व में व्यक्त किया जा सकता है। यह विश्लेषण
गणितीय रूप में या आरेखीय ढंग से किया जा सकता है। </p>

<p>◊ <b> ऋण - सर्विस अनुपात: </b> ऋण सेवा व्याप्ति अनुपात कंपनी
की दीर्घ अवधि की देयताओं को पूरा करने की सामर्थ्य का
मापदंड है। इस अनुपात को ऐसी राशि के रूप में व्यक्त
किया जाता है जिसे परियोजना ऋण उधार पर ब्याज तथा
मूलधन के लिए हर साल भुगतान करती है (या भुगतान करने
का प्रस्ताव करती है), अर्थात उस ऋण सर्विस के भुगतान
के लिए उपलब्ध निधियों की तुलना करते समय भुगतान की
जाने वाली ऋण सर्विस की राशि।  </p>

