कंपनियों पर प्राइवेट लिमिटेड कंपनी के उपबंध लागू होंगे
(अधिक जानकारी के लिए कृपया उत्पादक कंपनी अधिनियम
देखें)।

</p>

<p>◊ उत्पादक कंपनी की देयताएं उस शेयर पूंजी के मूल्य तक
