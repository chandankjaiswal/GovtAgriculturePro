सदस्य की देयता उसके द्वारा धारित शेयर पूंजी के मूल्य
तक सीमित होती है। उत्पादक कंपनी के निगमन के समय
न्यूनतम अधिकृत पूंजी 5 लाख रुपए होनी चाहिए। अधिकृत
पूंजी वह होती है जिसे उत्पादक कंपनी के संगम अनुच्छेद 
संगम ज्ञापन के माध्यम से उचित शेयर के रूप में जुटाने
के लिए कंपनी को अधिकृत किया जाता है। विशिष्ट रूप से
यह ऐसी पूंजी होती है जो निगमन के समय जुटाई जाती
है।
</p>
