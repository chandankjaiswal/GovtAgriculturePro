     <p><b>चरण 4 : संगम ज्ञापन एवं संगम अनुच्छेद*</b></p>
 <p>◊ संगम ज्ञापन तथा तथा संगम अनुच्छेद मुद्रित होने चाहिए
(वरीयता- कप्यूटर प्रिंट आउट जो पेपर के दोनों तरफ मुद्रित
हो)।</p>

<p>◊ संगम ज्ञापन एवं संगम अनुच्छेद पर विधिवत स्टाम्प0
लगवाएं। संगम ज्ञापन तथा संगम अनुच्छेद पर अपेक्षित
संख्या में अनुमोदनकर्ता/प्रवर्तकों द्वारा उनके ही हाथों से
हस्ताक्षर/अनुमोदन होना चाहिए, तथा उनके पिता के नाम,
पेशा, व्यवसाय तथा अनुमोदित किए गए शेयरों की संख्या का
उल्लेख होना चाहिए।
</p>
<p>◊ सुनिश्चित करें कि ज्ञापन एवं अनुच्छेद पर तारीख ऐसी डाली
हो जो स्टाम्प लगाने की तारीख के बाद की हो।
</p>

<p><b>चरण 5: उत्पादक कंपनी के निगमन के लिए कंपनी रजिस्ट्रार के
पास जमा किए जाने वाले दस्तावेज"
</b></p>
<p>◊ देय शुल्क” के साथ उस राज्य के कपनी रजिस्ट्रार के साथ
निम्नलिखित दस्तावेज दाखिल करें, जहां कंपनी का पंजीकृत
कार्यालय स्थित होना है
</p>
<p>◊ कपनी के गठन के लिए नाम की उपलब्धता की पुष्टि करने
वाले कंपनी रजिस्ट्रार के पत्र की प्रति संलग्न की जानी
चाहिए
</p>
<p>◊ संगम ज्ञापन एवं संगम अनुच्छेद पर विधिवत स्टाम्प लगा हो
तथा उन पर विधिवत हस्ताक्षर किए गए हों
</p>
<p>◊ पंजीकृत कार्यालय” की स्थिति के संबंध में फार्म 48 (पूरा
पता) या
</p>
<p>◊ निदेशकों" के विवरण के संबंध में फार्म 32 (दो प्रतियों में) या</p>
<p>◊ कंपनियों के गठन के संबंध में सभी एवं आनुषांगिक मामलों के
अनुपालन की घोषणा करने वाला फार्म 1 (स्टाम्प पेपर पर) 15
</p>
<p>◊ फार्म 29- निदेशक की सहमति</p>
<p>◊ यदि भुगतानकर्ता द्वारा हिन्दी में संगम ज्ञापन प्रस्तुत किया
जाता है, तो भुगतानकर्ता द्वार इस आशय का एक हलफनामा
प्रस्तुत करना होगा कि वे हिन्दी समझते हैं
</p>
<p>◊ पावर ऑफ अटॉर्नी।</p>

<p>कृपया ध्यान दें कि सभी सूचनाएं एवं फार्म कारपोरेट मामलों के
मंत्रालय की वेबसाइट ( http://www.mca.gov.in) पर उपलब्ध हैं
तथा यह कि फार्म सीधे खोले जा सकते हैं और ऑनलाइन दाखिल
किए जा सकते हैं।
</p>

<p><b>चरण 6 : निगमन प्रमाण पत्र</b></p>

<p>◊ जब कपनी रजिस्ट्रार इस बात से संतुष्ट हो जाते हैं कि कपनी
के निगमन के लिए सभी दस्तावेज जमा कर दिए गए हैं, तो
वह ज्ञापन, अनुच्छेदों तथा अन्य दस्तावेजों, यदि कोई हो, को
पंजीकृत करने तथा 30 दिन के अंदर 'निगमन प्रमाण पत्र जारी
करने के लिए बाध्य होते हैं, जो भाग 9क धारा 5200 ग 0)
की दृष्टि से कंपनी के गठन का निर्णायक प्रमाण होता है।
</p>

<p>◊ उत्पादक कंपनी का निगमन कंपनी रजिस्ट्रार द्वारा प्रदान
किए गए पंजीकरण प्रमाण पत्र में उल्लिखित ताशैख से लागू
होता है।
</p>

<p>◊ निगमन हो जाने पर कपनी विधिशास्त्रीय व्यक्ति अर्थात कानून
की नजरों में व्यक्ति बन जाती है। इसके शाश्वत उत्तराधिकारी
होते हैं अर्थात इसके सदस्य आ और जा सकते हैं परंतु
कंपनी तब तक चलती हैं जब तक कि कानून की प्रक्रिया का
अनुसरण करके इसे बंद नहीं किया जाता है।
</p>

<p>◊ इसकी एक सामान्य मुहर होती है जिसे किसी निदेशक की
उपस्थिति में कंपनी की ओर से निष्पादित सभी दस्तावेजों
पर लगाया जाता है तथा उस पर अधिकृत हस्ताक्षरकर्ता या
हस्ताक्षर कर्ताओं द्वारा हस्ताक्षर होने चाहिए।
