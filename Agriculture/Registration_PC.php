<h4 style="margin:35px">'उत्पादक कंपनी' के पंजीकरण के लिए चरणवार बुनियादी सूचनाओं
का वर्णन नीचे किया गया हैं</h4>
<p><b>चरण 1 : डिजिटल हस्ताक्षर प्रमाण पत्र (डीएससी):</b></p>
<p> सूचना प्रौद्योगिकी अधिनियम, 2000 में दस्तावेजों पर डिजिटल
हस्ताक्षर का प्रयोग करने का प्रावधान है ताकि इलेक्ट्रॉनिक रूप से
दाखिल किए गए दस्तावेजों की सुरक्षा एवं प्रमाणिकता का सुनिश्वय
हो सके। अब औपचारिक पंजीकरण प्रक्रिया की शुरुआत से पूर्व
कम से कम एक निदेशक या चेयरमैन का डिजिटल हस्ताक्षर प्राप्त
करना अनिवार्य हो गया है। यह सुरक्षित एवं प्रमाणित ढंग से किसी
दस्तावेज को इलेक्ट्रॉनिक रूप से प्रस्तुत करने का एक मात्र तरीका
है। इस प्रकार, कंपनियों द्वारा जो भी दस्तावेज/सूचनाएं दाखिल
की जाती हैं उनको डिजिटल हस्ताक्षर के प्रयोग के साथ दाखिल
करने की जरूरत होती है। इस तरह से कंपनी के लिए यह अनिवार्य
है कि वे किसी व्यक्ति के हस्ताक्षर को अधिकृत करें, जो दस्तावेजों
पर हस्ताक्षर करेगा। <br>

डीएससी के लिए निर्धारित आवेदन पत्र कारपोरेट मामलों के
मंत्रालय की वेबसाइट (अब इसके आगे इसे एमसीए की वेबसाइट कहा गया है) पर उपलब्ध है4। अपेक्षित सूचनाओं को दाखिल करने
के बाद आवेदन पत्र प्रमाणन एजेंसी5% के पास ऑनलाइन प्रस्तुत
करना होता है। विशिष्ट रूप से 4 या 2 वर्ष की वैधता के साथ
डिजिटल हस्ताक्षर प्रमाण पत्र जाशै किए जाते हैं। आरंभिक निर्गम की अवधि समाप्त हो जाने पर इनका नवीकरण कराया जा सकता है। डिजिटल हस्ताक्षर प्रमाण पत्र जारी करने के लिए निर्धारित
शुल्क 800 रुपए है। इसके अलावा, प्रमाणन एजेंसियां सेवा शुल्क
भी लेती हैं जो एजेंसी दर एजेंसी भिन्न होती है।

</p>

<p><b>चरण 2 : निदेशक पहचान संख्या (डीआईएन)</b></p>
<p> पहचान का प्रमाण (केवल पैन कार्ड, मतदाता पहचान पत्र, पासपोर्ट
या ड्राइविंग लाइसेंस स्वीकार किए जाते हैं) उपलब्ध कराकर किसी
शुल्क का भुगतान किए बगैर नोएडा, उत्तर प्रदेश स्थित कंपनी कार्य
प्रकोष्ठ से निदेशक पहचान संख्या ऑनलाइन प्राप्त किया जा सकता
है। निर्धारित फार्म कारपोरेट मामलों के मंत्रालय
उपलब्ध है तथा आवेदन ऑनलाइन किया जा सकता है।
</p>

<p><b>चरण 3 : उत्पादक कंपनी का नामकरण*</b></p>
<p>उत्पादक कंपनी की हैसियत का उपयुक्त ढंग से उल्लेख करते हुए
उत्पादक कपनी लिमिटेड” जैसे प्रत्याय का प्रयोग करके उत्पादक कंपनी का नामकरण करना चाहिए। नाम में “प्राइवेट”
शब्द का प्रयोग नहीं किया जाता है तथा इसकी अनुपस्थिति से इस
बात का संकेत नहीं मिलता कि कंपनी “सार्वजनिक” कपनी नहीं है।
</p>

<p>उत्पादक कंपनी के लिए नाम की उपलब्धता के लिए आवेदन करने
एवं चयन करने की प्रक्रिया इस प्रकार है :
</p> 
<p>◊ अधिक से अधिक पांच नामों के साथ कम से कम एक उपयुक्त
नाम का चयन तरजीही क्रम में करें, जो कंपनी के मुख्य
उद्देश्यों को इंगित करे।</p>

<p>◊ पोर्टल (  http://www.mca.gov.in ) पर “नामों की उपलब्धता
की जांच सेवा प्राप्त करके सुनिश्चित करें कि पहले से किसी
अन्य पंजीकृत कंपनी के नाम से चुने गए नामों की समानता
न हो तथा प्रतीक चिह्च एवं नाम (अनुचित प्रयोग निषेध)
अधिनियम, 950 के प्रावधानों का उल्लंघन न हो।
</p>
<p>◊ पोर्टल (http://www.mca.gov.in)पर लॉग इन करके
ई-फार्म  (ए)7 में नाम की उपलब्धता का सुनिश्चय करने के
लिए संबंधित कंपनी रजिस्ट्रार के पास आवेदन करें। इसके
साथ 500 रुपए8 के शुल्क का भुगतान करना होता है तथा
फार्म के साथ कपनी का प्रस्ताव करने वाले आवेदक का
डिजिटल हस्ताक्षर संलग्न करना होता है। यदि पांचों प्रस्तावित
नाम उपलब्ध नहीं होते हैं, तो कंपनी रजिस्ट्रार द्वारा आवेदक
को सूचित किया जाता है तथा बाद में आवेदक को उसी
आवेदन पर नए नाम के लिए आवेदन करना होता है।
</p>
<p>इसके अलावा, यदि जरूरत होती है, तो उत्पादक कंपनी का
नाम बदलने की आगे भी गुंजाइश रहती है। तथापि, बार-बार
नाम बदलना आसान नहीं होता है। कंपनी अधिनियम, 956 की
धारा 2। के अनुसार, निदेशक मंडल के दो तिहाई तथा सामान्य निकाय के एक तिहाई बहुमत से पारित प्रस्ताव तथा 500 रुपए के शुल्क के साथ आवेदन पत्र कंपनी रजिस्ट्रार को वरीयता क्रम
में चार अन्य विकल्पों तथा नए प्रस्तावित नाम के साथ प्रस्तुत
करना होता है।
 </p>
